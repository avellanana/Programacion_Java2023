package tema3_Calc;


/**
 * 
 * @author Ana
 *Esta clase es una librería de utilidades de
 *calculadora
 */

public class Calculadora {
	
	/**
	 * clase de utilidad de suma
	 * @param sumandoa primer sumando
	 * @param sumandob segundo sumando
	 * @return la suma de sumandoa y sumandob
	 */
	
	public int suma (int sumandoa, int sumandob) {
		return sumandoa + sumandob;
	}
	
	/**
	 * clase de utilidad de multiplicacion
	 * @param multiplicandoa primer factor
	 * @param multiplicandob segundo factor
	 * @return la multiplicación
	 */
	
	public int multiplicacion (int multiplicandoa, int multiplicandob) {
		return multiplicandoa * multiplicandob;
	}

}
